	<h2>Music</h2>
		<p>The film's soundtrack features the song "Accidentally in Love" by Counting Crows, which the band's singer and songwriter Adam Duritz believes "fits into the movie because it's the story of people who fall in love who weren't supposed to fall in love." The soundtrack was composed by Harry Gregson Williams.</p>

		<p>Shrek 2: Motion Picture Soundtrack is the soundtrack for the hit animated comedy film Shrek 2 and was released in May 2004, to accompany the release of the film. The soundtrack reached the Top 10 on the Billboard 200 and number 1 on the Soundtrack Albums as well as topping the albums chart in Australia. The lead single, "Accidentally in Love" by the Counting Crows, reached the Top 10 of the Billboard Adult Top 40, the Top 20 of singles charts worldwide and was nominated for an Academy Award for Best Song. The soundtrack also features two versions of the 1980s Bonnie Tyler hit "Holding Out for a Hero".</p>

